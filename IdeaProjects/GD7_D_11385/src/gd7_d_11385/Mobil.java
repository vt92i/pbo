package gd7_d_11385;

public class Mobil {
  private final String nama;
  private final int kapasitas;

  public Mobil(String nama, int kapasitas) {
    this.nama = nama;
    this.kapasitas = kapasitas;
  }

  public String getNama() {
    return nama;
  }

  public int getKapasitas() {
    return kapasitas;
  }
}
