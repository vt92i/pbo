package gd7_d_11385;

public class PairThree<X, Y, Z> {
  private final X value1;
  private final Y value2;
  private final Z value3;
  private final int value4;

  public PairThree(X value1, Y value2, Z value3, int value4) {
    this.value1 = value1;
    this.value2 = value2;
    this.value3 = value3;
    this.value4 = value4;
  }

  public X getValue1() {
    return value1;
  }

  public Y getValue2() {
    return value2;
  }

  public Z getValue3() {
    return value3;
  }

  public int getValue4() {
    return value4;
  }
}
